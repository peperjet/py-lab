print(a[0])


# 6.리스트 [1, 2, 3, 4, 5]를 역순 리스트로 만들어 출력하라.
a=[1,2,3,4,5]
a.reverse()
print(a)


# 7.리스트 [1, 2, 3, 2, 2, 4]에서 숫자 2가 몇 번 등장하는지 출력하라.
a=[1,2,3,2,2,4]
print(a.count(2))


# 8.리스트 [1, 2, 3]에 숫자 4를 추가한 뒤 출력하라.
