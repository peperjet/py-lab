a.append(4)
print(a) 


# 9.리스트 [1, 2, 3, 4, 5]에서 인덱스 1 ~ 3까지 슬라이싱하여 출력하라.
a=[1,2,3,4,5]
print(a[1:4])


# 10.리스트 안의 숫자들을 모두 문자열로 바꾼 리스트를 만들어 출력하라.
a=[1,2,3,4,5]
str_a = [str(i) for i in a]
print(str_a)